    <div class="middle-content content">
        <p>Метод тиснения позволяет наносить долговечные рельефные изображения на мягкие и пластичные материалы: кожу и кожзаменитель, бумагу и картон, дерево. Он активно применяется для брендирования записных книжек, блокнотов, ежедневников, фирменных бланков, пригласительных билетов, визиток и визитниц, кожаных чехлов, портмоне и многих других статусных изделий.</p>
        <p>В настоящее время наиболее распространена технология горячего тиснения, когда изображение выдавливается на мягкой обложке, упаковочном материале и т.п. заранее изготовленным металлическим штампом-клише, разогретым в термопрессе.</p>
        <p>Различается два основных вида горячего тиснения: слепое (блинтовое) и фольгой. В первом случае цвет надпись или логотипа лишь незначительно отличается от цвета материала за счет эффекта «прижигания». Во втором – между штампом-клише и материалом прокладывается специальная фольга, которая впаивается в верхний слой поверхности оттиска. Цвет фольги может быть золотым, серебряным или бронзовым, что добавляет рисунку контрастности, делает его еще более презентабельным.</p>
