        <p>Гравировка как вид декоративного искусства ассоциируется с чем-то благородным, изысканным и элитарным. Так украшались рыцарские доспехи, холодное и огнестрельное оружие, особенно наградное. Техника также широко применяется в ювелирном деле. Современные же лазерные технологии позволяют быстро и недорого наносить на металлические поверхности самые сложные и высокохудожественные надписи, рисунки и орнаменты, которые невозможно выполнить вручную.</p>
