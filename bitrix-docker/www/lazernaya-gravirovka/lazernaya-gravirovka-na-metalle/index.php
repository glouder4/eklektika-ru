        <h3>Требования к макетам</h3>
        <hr>
        <ul>
            <li>Документ предоставляется в формате CorelDRAW 11 масштабом 1:1. Изображение должно иметь один слой — фигуры, составленные из нескольких производных фигур, слиты в одну. Достигается это функцией Weld (слияние).</li>
            <li>В случае если изображение (логотип) состоит из нескольких разноцветных фигур, расположенных друг над другом (перекрывающих друг друга), то в нижних фигурах должны быть пробиты дырки, с помощью функции Trim (вырубка).</li>
            <li>Текст необходимо перевести в кривые с помощью Convert to Curves (распашное меню Arrange). Либо прикрепите используемые шрифты к вашему файлу.</li>
            <li>При кегле 6 и ниже лучше не использовать шрифты с засечками, например А Б Г, они могут быть сложны при воспроизведении.</li>
            <li>Количество узлов (кривая Безье) ваших объектов не должно быть больше 300. Для изменения ситуации воспользуйтесь инструментом Shape Tool.</li>
            <li>Ширина линий и пробелов для тампопечати не должна быть меньше 0,15 мм.</li>
            <li>Ширина линии или пробела при нанесении методом лазерной гравировки не должна быть менее 0,35 мм. Высота строчной буквы — не менее 1 мм. Необходимо учитывать, что линия Hairline, считывается лазерными установками как линия вырезки и не может использоваться в качестве обводки.</li>
            <li>Если макет будет использоваться для тиснения на коже, кожзаменителе или пластиках, то ширина линий и пробелов должна составлять не менее 0,4 мм.</li>
        </ul>
