        <h2>Виды лазерной гравировки на металле</h2>
        <p>Существует две разновидности: векторная и растровая. В первом случае лазерный луч наносит штрихи-векторы, во втором — точки-пиксели. Векторная гравировка подходит для нанесения надписей и логотипов, когда важна не столько детализация, сколько масштабируемость изображения, возможность его увеличивать или уменьшать без потери качества. Растровым методом, как правило, наносятся сложные рисунки и орнаменты, фотоизображения, когда необходимо воспроизвести не только множество деталей, но и тонов.</p>
