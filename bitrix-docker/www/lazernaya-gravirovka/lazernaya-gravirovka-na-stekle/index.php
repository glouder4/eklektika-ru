        <h2>Технология лазерной гравировки на стекле</h2>
        <p>Нанесение изображения производится путем выжигания тончайшим лазерным лучом на стеклянной поверхности.
            Обработанный лазером участок теряет прозрачность и становится видимым. Существует два метода такой
            гравировки: векторная и растровая. В первом случае луч создает последовательность прямых или изогнутых
            линий, штрихов различной толщины, глубины и плотности. При растровом методе выжигаются отдельные
            точки-пиксели, различная последовательность и плотность которых также создает итоговый рисунок с
            полутонами.</p>
        <p>&nbsp;</p>
        <h3>Технология нанесения</h3>
        <p>В отличие от нанесения изображения на стеклянную поверхность, при создании 3D-фигур внутри кристаллов
            лазерный луч не выжигает, а свободно проходит сквозь толщь стекла. Чтобы появилось изображение, под углом к
            одному лучу направляется второй такой же луч, и при их пересечении происходит микровзрыв, формирующий точку
            размером в десятки микрон. Объемный рисунок в 5–6 куб. см может состоять из десятков тысяч таких точек.</p>
