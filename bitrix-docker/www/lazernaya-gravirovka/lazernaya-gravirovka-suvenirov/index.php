        <h3>Основные требования к макетам:</h3>
        <p><strong>Векторное изображение в формате CDR (CorelDRAW) или AI (Adobe Illustrator).</strong></p>
        <p><strong>Для лазерной гравировки по пластику (Spectrum):</strong></p>
        <p>минимальная толщина линии — 0,2 мм</p>
        <p>максимальный размер нанесения — 600х300 мм</p>
        <p><strong>Для лазерной гравировки по металлу (AlumaMark):</strong></p>
        <p>минимальная толщина линии — 0,13 мм</p>
        <p>максимальный размер нанесения — 100х100 мм</p>
        <hr>
        <strong>Требования к макетам:</strong>
        <p>Файл логотипа должен соответствовать следующим требованиям: Документ предоставляется в формате CorelDRAW 11
            масштабом 1:1 Изображение должно иметь один слой - фигуры, составленные из нескольких производных фигур,
            слиты в одну. Достигается это функцией Weld (слияние):</p>
        <p>В случаи если изображение (логотип) состоит из нескольких разноцветных фигур расположенных друг над другом
            (перекрывающим друг друга) то в нижних фигурах должны быть пробиты дырки, с помощью функции Trim
            (вырубка).</p>
        <p>Текст необходимо перевести в кривые, воспользуйтесь Convert to Curves, распашное меню Arrange. Либо
            прикрепите используемые шрифты к вашему файлу.</p>
        <p>При кегле 6 и ниже лучше не использовать шрифты с засечками, например А Б Г, они могут быть сложны при
            воспроизведении на выбранном изделии.</p>
        <p>Количество узлов (кривая Безье) ваших объектов не должно превышать 300 узлов! Для изменения ситуации
            воспользуйтесь инструментом Shape Tool.</p>
        <p>Ширина линий и пробелов для тампопечати не должна быть меньше 0,15 мм.</p>
        <p>Ширины линии или пробела при нанесении методом лезерной гравировки не должна быть менее 0,35 мм. Высота (не
            заглавной) буквы не менее 1 мм. Необходимо учитывать, что линия Hairline, считываться лазерными установками
            как линия вырезки и ни в коем случаи не должна использоваться в качестве обводки.</p>
        <p>Если макет будет использоваться для тиснения на коже или кожзаменителе, а так же на пластиках, то ширина
            линий и пробелов должна составлять не менее 0,4 мм</p>
        <p>Внимание! Исполнитель оставляет за собой право вносить незначительные изменения в макет (уменьшить,
            увеличить, толщину линий, изменить расположение (выше, ниже, левее, правее), если того требуют
            технологические особенности материала или технические возможности оборудования и если эти изменения не
            влекут за собой искажения логотипа.</p>
