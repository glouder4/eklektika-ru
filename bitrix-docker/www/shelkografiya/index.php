        <h2>Печать методом шелкографии и термотрансфера на подарках и сувенирах</h2>
        <p>Обе технологии относятся к трафаретной печати. При нанесении изображения для каждого цвета делаем отдельный шаблон.</p>
        <p>Процесс изнутри:</p>
        <ul>
            <li>сетка из металлических или нейлоновых нитей покрывается фоточувствительной эмульсией;</li>
            <li>под лампой засвечиваются и твердеют участки «ненужных» цветов. Всё остальное смывается водой — так получается трафарет для переноса выбранного цвета.</li>
            <li>форму для печати закрепляют на формодержателе. Затем ракелем продавливают сквозь нее краску. Толщина слоя — от 10–12 микрон до 500 микрон. Можно делать объемные изображения.</li>
            <li>после того как слой просох, ставится новая форма и наносится следующий цвет.</li>
        </ul>
        <p>Шелкография переносит изображения сразу на ткань, готовую одежду или аксессуары. Печать возможна только на плоских поверхностях. Для изогнутых используется шелкотрансфер. Логотип сначала печатается на промежуточном носителе, а потом с помощью термопресса переносится на конечное изделие.</p>
        <h2>Требования к макетам</h2>
        <p>Логотип должен быть переведен в векторный формат CDR (CorelDRAW) или AI (Adobe Illustrator) без эффектов и прозрачностей. Минимальная толщина линий — 0,3 мм. Цвета — по Pantone Solid Coated.</p>
        <p>При трафаретной печати на цветной одежде рекомендуем использовать подложку. Тогда оттенки будут точно соответствовать макету. Подложка считается отдельным цветом.</p>
        <h2>Преимущества шелкографии и шелкотрансфера:</h2>
        <ol>
            <li>100%-я цветопередача из палитры Pantone, высокая детализация.</li>
            <li>Краски для печати подбираются под материал по химическому составу, чтобы увеличить стойкость рисунка к выгоранию, горячей воде, моющим средствам.</li>
            <li>Возможно нанесение крупноформатных изображений — до А3.</li>
            <li>Формы можно использовать многократно. Это уменьшает себестоимость печати на оптовых тиражах.</li>
        </ol>
        <p>Технология не передает градиентные переходы — это касается и шелкотрансфера. Можно использовать металлизированные, флуоресцентные краски, глиттеры, добавлять спецэффекты (плюш, глянец, пуфф и т. д.).</p>
        <p>Минимальный заказ — 50 шт. Предлагаем услуги по подготовке макетов к печати, доработке, сканированию. Условия для тиражей от 5 тыс. экземпляров рассчитываются индивидуально.</p>
